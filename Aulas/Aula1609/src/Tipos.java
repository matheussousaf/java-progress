
public class Tipos {
	public static int valor = 10;
	public static boolean verdadeiro = false;
	public static int soma = valor + 8;
	public static int outroValor = valor;
	public static double numeroEstranho = 234.987;
	public static char caractere = 'P';
	
	public static void main(String[] args) {
		System.out.println("Valor: " + valor);
		System.out.println("Boolean: " + verdadeiro);
		System.out.println("Soma: " + soma);
		System.out.println("Outro valor: " + outroValor);
		System.out.println("Número estranho: " + numeroEstranho);
		System.out.println("Caractere: " + caractere);
		
	}
}
